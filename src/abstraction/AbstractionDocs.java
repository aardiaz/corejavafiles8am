package abstraction;

public class AbstractionDocs {
	
	/*
	 * ----------------------- Abstraction ----------------------------
	 *   # process of hiding implementation details in the program
	 *     is known as abstraction.
	 *     
	 *   # To achieve abstraction :
	 *     a> Abstract class
	 *      # class which contains abstract(unimplemented methods) methods and non-abstract(implemented methods) methods
	 *        is known as abstract class.
	 *      
	 *        # Abstract method :
	 *           -> method which does not have body/implementation.
	 *           e.g.
	 *             
	 *              abstract  void sum();
	 *              abstract int getValue(String msg);
	 *              
	 *       # every  abstract class must have at least one child class
	 *         and child class must override abstract methods.
	 *         
	 *       # we can't create object of abstract class.
	 *       # level of abstraction 0-100%
	 *       
	 *              syntax :
	 *              
	 *                 abstract class class_name{
	 *                 
	 *                      //abstract methods
	 *                      
	 *                      //non-abstract methods
	 *                 }
	 *      
	 *     b> Interface :
	 *      # same as class which contains public abstract methods and
	 *        public static final variables.
	 *        
	 *      # we cant create object of interface.
	 *      
	 *      # every interface must have at least one implementation class
	 *        and impl. class must override abstract methods.
	 *        
	 *      # level of abstraction 100%
	 *      
	 *           syntax :
	 *           
	 *           interface interfce_name{
	 *           
	 *                //public abstract methods
	 *               
	 *           }
	 */

}
